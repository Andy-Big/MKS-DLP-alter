/**
  * @brief  The application entry point.
  * @retval int
  */

int main()
{
	HAL_Init();
	SystemClock_Config();
	SCB->VTOR = FLASH_BASE | 0x10000;
	
	MX_RTC_Init();
	__HAL_RCC_RTC_ENABLE();
	
	LCD_Initializtion();
	
	LCD_SetCursor(100, 100);
	LCD_PutChar_16x24(110, 100, 'T', Yellow, Black);
	LCD_PutChar_16x24(126, 100, 'E', Yellow, Black);
	LCD_PutChar_16x24(142, 100, 'S', Yellow, Black);
	LCD_PutChar_16x24(158, 100, 'T', Yellow, Black);

	volatile uint16_t pc = 0;

	LCD_SetCursor(100, 150);
	LCD_SetWindows(100, 150, 50, 2);
	for (uint16_t i = 0; i < 70; i++)
	{
		LCD_ReadRAM_Prepare();
		pc = LCD_ReadRAM();
		LCD_WriteRAM_Prepare();
		LCD_WriteRAM(pc|White);
		LCD_ReadRAM_Prepare();
		pc = LCD_ReadRAM();
		pc = LCD_ReadRAM();
		pc--;
	}
	for (uint16_t i = 0; i < 50; i++)
	{
		LCD_ReadRAM_Prepare();
		pc = LCD_ReadRAM();
		LCD_WriteRAM_Prepare();
		LCD_WriteRAM((White + pc) / 2);
	}
	
	while(1);
	
}

